from .. import loader
import os
import re
import urllib.parse


@loader.tds
class AmeChangeLoaderText(loader.Module):
    """–ú–æ–¥—É–ª—å –¥–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è —Ç–µ–∫—Å—Ç–∞ –∏ –±–∞–Ω–Ω–µ—Ä–∞ –∑–∞–≥—Ä—É–∑—á–∏–∫–∞. 05"""

    strings = {"name": "AmeChangeLoaderText"}

    PLACEHOLDERS = {
        "version": "'.'.join(map(str, version))",
        "build": "build",
        "build_hash": "build[:7]",
        "upd": "upd",
        "web_url": "web_url",
    }

    def _replace_placeholders(self, text):
        for key, value in self.PLACEHOLDERS.items():
            text = text.replace(f"{{{key}}}", value)
        return text

    async def updateloadercmd(self, message):
        """
        –ö–æ–º–∞–Ω–¥–∞ –¥–ª—è –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è —Ç–µ–∫—Å—Ç–∞ –∏–ª–∏ –±–∞–Ω–Ω–µ—Ä–∞ –∑–∞–≥—Ä—É–∑—á–∏–∫–∞.
        """
        cmd = message.raw_text.split(maxsplit=1)
        if len(cmd) == 1:
            await message.edit(
                "<b>üìã –°–ø—Ä–∞–≤–∫–∞ –ø–æ AmeChangeLoaderText:</b>\n\n"
                "‚Ä¢ <code>.updateloader https://site.com/banner.mp4</code> - –ó–∞–º–µ–Ω–∏—Ç—å –±–∞–Ω–Ω–µ—Ä\n"
                "‚Ä¢ <code>.updateloader —Ç–µ–∫—Å—Ç</code> - –ó–∞–º–µ–Ω–∏—Ç—å —Ç–µ–∫—Å—Ç\n"
                "‚Ä¢ <code>.updateloader —Ç–µ–∫—Å—Ç —Å placeholder</code> - –ó–∞–º–µ–Ω–∏—Ç—å —Ç–µ–∫—Å—Ç —Å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏:\n"
                "   {version} - –≤–µ—Ä—Å–∏—è\n"
                "   {build} - –ø–æ–ª–Ω—ã–π –±–∏–ª–¥\n"
                "   {build_hash} - –∫–æ—Ä–æ—Ç–∫–∏–π —Ö–µ—à –±–∏–ª–¥–∞\n"
                "   {upd} - —Å—Ç–∞—Ç—É—Å –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è\n"
                "   {web_url} - –≤–µ–±-URL\n\n"
                "–ü—Ä–∏–º–µ—Ä:\n"
                "<code>.updateloader –°—Ç–∞—Ç—É—Å {upd} –í–µ–± {web_url}</code>\n\n"
            )
            return
        try:
            args = cmd[1].strip()
            main_file_path = os.path.join("hikka", "main.py")

            with open(main_file_path, "r", encoding="utf-8") as f:
                content = f.read()
            # –ë–æ–ª–µ–µ —Ç–æ—á–Ω—ã–π –ø–∞—Ç—Ç–µ—Ä–Ω –¥–ª—è –ø–æ–∏—Å–∫–∞ –±–ª–æ–∫–∞

            animation_block_pattern = (
                r"await\s+client\.hikka_inline\.bot\.send_animation\(\n"
                r'.*?caption=\(\s*"[^"]*"\s*\),\n'
                r".*?\)"
            )

            # –ù–∞—Ö–æ–¥–∏–º –≤—Å–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è

            animation_blocks = re.findall(animation_block_pattern, content, re.DOTALL)

            if not animation_blocks:
                raise ValueError("–ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞–π—Ç–∏ –±–ª–æ–∫ –æ—Ç–ø—Ä–∞–≤–∫–∏ –∞–Ω–∏–º–∞—Ü–∏–∏ –≤ main.py")
            if self._is_valid_url(args):
                # –ó–∞–º–µ–Ω–∞ URL –±–∞–Ω–Ω–µ—Ä–∞

                new_block = re.sub(
                    r'"https://[^"]+\.mp4"', f'"{args}"', animation_blocks[0]
                )
            else:
                # –ó–∞–º–µ–Ω–∞ —Ç–µ–∫—Å—Ç–∞

                user_text = self._replace_placeholders(args)
                # –ë–æ–ª–µ–µ –Ω–∞–¥–µ–∂–Ω–∞—è –∑–∞–º–µ–Ω–∞ caption

                new_block = re.sub(
                    r'caption=\(\s*"[^"]*"\s*\)',
                    f'caption=("{user_text}")',
                    animation_blocks[0],
                )
            # –ó–∞–º–µ–Ω—è–µ–º –≤–µ—Å—å –±–ª–æ–∫

            content = content.replace(animation_blocks[0], new_block)

            with open(main_file_path, "w", encoding="utf-8") as f:
                f.write(content)
            await message.edit(
                f"‚úÖ –û–±–Ω–æ–≤–ª–µ–Ω–æ –Ω–∞: <code>{args}</code>\n–ù–∞–ø–∏—à–∏—Ç–µ <code>.restart -f</code>"
            )
        except Exception as e:
            await message.edit(f"‚ùå –û—à–∏–±–∫–∞: <code>{str(e)}</code>")

    def _is_valid_url(self, url):
        """
        –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ URL –≤–∞–ª–∏–¥–Ω—ã–º –∏ –æ–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è –Ω–∞ .mp4.
        """
        try:
            result = urllib.parse.urlparse(url)
            return all([result.scheme, result.netloc]) and url.lower().endswith(".mp4")
        except:
            return False
